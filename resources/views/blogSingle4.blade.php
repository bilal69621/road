                        <p>Sudden soft brakes can be an annoying and dangerous issue to deal with. Your car won’t stop as fast anymore making it totally unsafe to drive both for you and other vehicles on the road. There are many reasons that could cause this issue and it is always a good idea to have it checked by a mechanic even if you manage to temporarily fix the issue.</p>
